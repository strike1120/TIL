
public class Third {
	public static void main(String[] args) {
		//반지름이 5인 원의 넓이를 구하여 출력
		System.out.println(3.14 * Math.pow(5, 2)); //제곱근을 구하는 함수
		
		//반지름이 5인 원의 둘레를 구하여 출력
		System.out.println(2 * 3.14 * 5);
		
		// 삼각형의 밑변이 5, 높이 7일 때의 면적
		System.out.println(5 * 7 / 2);
		System.out.println(5 * 7 * 0.5);
	}
}
