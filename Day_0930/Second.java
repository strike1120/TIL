
public class Second {
	public static void main(String[] args) {
		System.out.println(2022); // 2022(int), 2022L (long)
		System.out.println(42.195); //42.195(double) , 42.195F(float)
		System.out.println("서울시 강남구"); // 문자열은 double quotation
		System.out.println('@'); //문자형은 single quotation
		System.out.println(true); // boolean - true
		System.out.println(false); // boolean - false
		
		// Data Type(자료형) - primitive, reference
		
		// --- primitive type (기본 자료형) ---
		/* 1) 정수형 : byte(8bit), short(2byte), int(4byte), long(8byte)
		 *      정수범위 : -128~127
		 * 2) 실수형 : float(4byte), double(8byte)
		 * 3) 문자형 : char(2byte)
		 * 4) 부울형 : boolean(1byte) - 참이나 거짓
		 */
	}
}
