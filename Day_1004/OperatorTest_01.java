
public class OperatorTest_01 {
	public static void main(String[] args) {
		int result;
		result = 10 + 20;
		System.out.println(result);
		
		result = 10 - 20;  // 변수 선언은 최초 한번만.
		System.out.println(result);
		
		result = 10 * 20;
		System.out.println(result);
		
		result = 10 / 20; //정수 나누기 정수 -> 정수
		System.out.println(result);
		
		result = 10 % 20; //나머지
		System.out.println(result);
	}

}
