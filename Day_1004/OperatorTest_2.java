
public class OperatorTest_2 {
	public static void main(String[] args) {
		double result;
		double val01=10, val02=20; // 10-> 10.0
		
		result = val01 + val02;
		System.out.println(result);
		
		result = val01 - val02;
		System.out.println(result);
		
		result = val01 * val02;
		System.out.println(result);
		
		result = val01 / val02;
		System.out.println(result);
		
		result = val01 % val02;
		System.out.println(result);
	}

}
